package gwt.material.design.incubator.client.question.base.constants;

public enum  Rating {

    POOR(1, "Poor"),
    FAIR(2, "Fair"),
    NEUTRAL(3, "Neutral"),
    GOOD(4, "Good"),
    EXCELLENT(5, "Excellent");

    private int type;
    private String name;

    Rating(int type, String name) {
        this.type = type;
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public String getName() {
        return name;
    }
}
